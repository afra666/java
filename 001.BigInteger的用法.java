package p;
import java.math.BigInteger;
import java.util.Scanner;
public class Main {
	public static void main(String[] args) {
		Scanner scanner=new Scanner(System.in);
		BigInteger b1=scanner.nextBigInteger();
		BigInteger b2=new BigInteger("10007");	
		scanner.close();
		BigInteger add,subtract,multiply,divide,remainder;//加减乘除余
		
		
		
		
		//加减乘除余赋值
		add           = b1.add(b2)    ;
		subtract      = b1.subtract(b2)   ;
		multiply      = b1.multiply(b2)   ;
		divide        = b1.divide(b2)   ;
		remainder     = b1.remainder(b2)   ;
		
		
		//输出
		System.out.println("/===========================================/");
		System.out.println("===b1="+b1);
		System.out.println("===b2="+b2);
		System.out.println("/===========================================/");
		System.out.println("b1+b2="+add);
		System.out.println("b1-b2="+subtract);
		System.out.println("b1*b2="+multiply);
		System.out.println("b1/b2="+divide);
		System.out.println("b1%b2="+remainder);
	}

}
